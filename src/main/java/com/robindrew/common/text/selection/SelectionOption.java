package com.robindrew.common.text.selection;

/**
 * The available selection options.
 */
public enum SelectionOption {

	/** Optional selection. */
	OPTIONAL,
	/** No move after selection. */
	NO_MOVE,
	/** Include delimiters in selection. */
	INCLUDE_DELIMITERS,
	/** Ignore case in selection. */
	IGNORE_CASE;

}